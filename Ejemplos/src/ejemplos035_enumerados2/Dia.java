package ejemplos035_enumerados2;

// un simple ejemplo donde se declara enum fuera de cualquier clase 
// (Nota la palabra enum en lugar de la palabra class) 

enum Dia {
    LUNES, MARTES, MIERCOLES, JUEVES, VIERNES, SABADO, DOMINGO;
}

// el sentido de los enumerados es acotar las opciones de dia en este
// caso a que SOLO se pueda elegir entre:
// LUNES, MARTES, MIERCOLES, JUEVES, VIERNES, SABADO, DOMINGO;